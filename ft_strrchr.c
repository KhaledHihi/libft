#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int	i;
	int	e;

	i = 0;
	e = 0;
	while (s[e])
		e++;
	while (i <= e)
	{
		if (s[e] == (char)c)
			return ((char *)s + e);
		e--;
	}
	return (NULL);
}
