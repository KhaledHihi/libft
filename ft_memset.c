#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s;

	i = 0;
	s = (char *)b;
	while (s[i] && i < len)
	{
		s[i] = (char)c;
		i++;
	}
	return (b);
}
